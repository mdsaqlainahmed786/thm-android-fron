            dialog = Dialog(context)
            dialog?.let { dlg ->
                val binding = DialogEmojiEntryBinding.inflate(dlg.layoutInflater)

                // Set background to transparent
                dlg.window?.setBackgroundDrawable(ColorDrawable(Color.TRANSPARENT))

                dlg.setContentView(binding.root)
                dlg.setCancelable(true)
                dlg.setCanceledOnTouchOutside(true)

//                // Set up RecyclerView
//                val emojiAdapter = EmojiAdapter(emojiList) { selectedEmoji ->
//                    onEmojiConfirmed(selectedEmoji)
//                    hide()  // Close dialog on emoji selection
//                }

                val emojiAdapter = EmojiAdapter(emojiList,::onEmojiSelected)
                binding.emojiRecyclerView.adapter = emojiAdapter



                // Set layout size and gravity
                dlg.window?.apply {
                    setLayout(WindowManager.LayoutParams.MATCH_PARENT, WindowManager.LayoutParams.MATCH_PARENT)
                    attributes?.gravity = Gravity.CENTER
                }
            }
        }
        dialog?.show()
    }

    private fun onEmojiSelected(selectedEmoji: String) {
        onEmojiConfirmed(selectedEmoji)
        hide()
    }

    fun hide() {
        dialog?.dismiss()
    }


